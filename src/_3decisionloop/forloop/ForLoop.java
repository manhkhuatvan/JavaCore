package _3decisionloop.forloop;

public class ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("Value of i: " + i);
        }

        int m = 4;
        for (System.out.println("Happy"); m <= 10; System.out.println("So funny")) {
            System.out.println("Vong lap chua phuong thuc trong bieu thuc");
            m++;
        }
    }
}
